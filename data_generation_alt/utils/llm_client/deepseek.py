from typing import Optional, List, Dict, Any
import requests
import json

class DeepSeekClient:
    """
    DeepSeek API client.
    Code generated by AI.
    Not sure if this is the correct way to use the DeepSeek API.
    """
    def __init__(self, api_key: str, base_url: Optional[str] = None):
        self.api_key = api_key
        self.base_url = base_url or "https://api.deepseek.com/beta"
        self.headers = {
            "Authorization": f"Bearer {self.api_key}",
            "Content-Type": "application/json"
        }

    def chat_completion(self, model: str, messages: List[Dict[str, str]], temperature: float = 0.5, n: int = 1) -> Dict[str, Any]:
        """
        Call DeepSeek's chat completion API
        """
        try:
            endpoint = f"{self.base_url}/chat/completions"
            payload = {
                "model": model,
                "messages": messages,
                "temperature": temperature,
                "n": n
            }
            
            response = requests.post(
                endpoint,
                headers=self.headers,
                data=json.dumps(payload)
            )
            
            response.raise_for_status()  # 抛出HTTP错误（如果存在）
            return response.json()
        except Exception as e:
            raise RuntimeError(f"DeepSeek API calling FAILURE: {e}")